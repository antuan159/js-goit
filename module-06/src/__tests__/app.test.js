const notepad = new Notepad(initialNotes);

test('create notepad model and init', () => {
  expect(notepad.notes).toEqual(arrInit);
});

test('save notes in model', () => {
  notepad.saveNote({
    id: 'id-3',
    title: 'Get comfy with Frontend frameworks',
    body:
      'First must get some general knowledge about frameworks, then maybe try each one for a week or so. Need to choose between React, Vue and Angular, by reading articles and watching videos.',
    priority: Notepad.Priority.NORMAL,
  });

  notepad.saveNote({
    id: 'id-4',
    title: 'Winter clothes',
    body:
      "Winter is coming! Need some really warm clothes: shoes, sweater, hat, jacket, scarf etc. Maybe should get a set of sportwear as well so I'll be able to do some excercises in the park.",
    priority: Notepad.Priority.LOW,
  });

  expect(notepad.notes).toEqual(arrSave);
});

test('change priority', () => {
  notepad.updateNotePriority('id-4', Notepad.Priority.NORMAL);
  expect(notepad.notes).toEqual(arrPriorityUp);
});

test('filter by query "html"', () => {
  expect(notepad.filterNotesByQuery('html')).toEqual(arrFilterQueryHtml);
});

test('filter by query "javascript"', () => {
  expect(notepad.filterNotesByQuery('javascript')).toEqual(arrFilterQueryJava);
});

test('filter by priority "normal"', () => {
  expect(notepad.filterNotesByPriority(Notepad.Priority.NORMAL)).toEqual(arrPriorityNormal);
});

test('Update content"', () => {
  const notepadUpdate = new Notepad(arrSave);
  notepadUpdate.updateNoteContent('id-3', {
    title: 'Get comfy with React.js or Vue.js',
  });
  expect(notepadUpdate.notes).toEqual(arrUpdate);
});

test('delete "id-2"', () => {
  const notepadDelete = new Notepad(arrSave);
  notepadDelete.deleteNote('id-2');
  expect(notepadDelete.notes).toEqual(arrDelete);
});
